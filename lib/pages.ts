export const getPageBySlug = (slug: string): Page | undefined =>
  PAGES.find(p => p.slug === slug);

export const getPublishedPages = (): Page[] =>
  PAGES.filter(p => p.published);

export const getPublishedTools = (): Page[] =>
  PAGES.filter(p => p.published && p.type === "tool");

export const getPublishedGuides = (): Page[] =>
  PAGES.filter(p => p.published && p.type === "article");

export const getAllTools = (): Page[] =>
  PAGES.filter(p => p.type === "tool");

export const getAllGuides = (): Page[] =>
  PAGES.filter(p => p.type === "article");

export const getPublishedByCategory = (category: Category): Page[] =>
  PAGES.filter(p => p.published && p.category === category);

/**
 * Return 2-3 related *published* pages for a given page.
 * Tier 1: declared related slugs that are published.
 * Tier 2: other published pages in the same category.
 * Tier 3: any other published page (so we always surface 2-3, even when the
 *         cluster is still thin early on).
 */
export function getRelated(slug: string, limit = 3): Page[] {
  const page = getPageBySlug(slug);
  if (!page) return [];
  const result: Page[] = [];
  const push = (p: Page) => {
    if (p.slug === slug) return;
    if (!p.published) return;
    if (result.find(r => r.slug === p.slug)) return;
    result.push(p);
  };

  // Tier 1 — declared related, in the order the author wrote them.
  for (const s of page.related || []) {
    const p = getPageBySlug(s);
    if (p) push(p);
    if (result.length >= limit) return result.slice(0, limit);
  }

  // Tier 2 — same-category filler.
  for (const p of PAGES) {
    if (p.category === page.category) push(p);
    if (result.length >= limit) return result.slice(0, limit);
  }

  // Tier 3 — anything else published, so every page has at least a couple.
  for (const p of PAGES) {
    push(p);
    if (result.length >= limit) return result.slice(0, limit);
  }

  return result.slice(0, limit);
}

export function pageHref(page: Page): string {
  return page.type === "tool" ? `/tools/${page.slug}` : `/guides/${page.slug}`;
}

